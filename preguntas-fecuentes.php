											1 ¿Qué es SISOCS y para qué se utiliza?</br>
El Sistema de Información y Seguimiento a Obras y Contratos de Supervisión “SISOCS” es la
herramienta creada por Decreto Ejecutivo PCM 02-2015, mediante la cual se publica y difunde
información relevante de los procesos de planificación, adquisición, contratación y ejecución
de los proyectos de infraestructura pública.</br>

2. ¿Cuáles son las instituciones que ingresan proyectos?</br>
De acuerdo al Decreto Ejecutivo PCM02-2015 (link a decreto) las entidades que forman parte
del Gabinete Central de Infraestructura Productiva están obligadas a divulgar información en
SISOCS. Estas instituciones son: La Secretaría de Infraestructura y Servicios Públicos (INSEP) a
través de la Dirección General de Carreteras(DGC), el Fondo Vial, la Secretaría de Salud
(SESAL), la Secretaría de Educación (SEDUC), Inversión Estratégica de Honduras (INVEST-H), la
Empresa Nacional Portuaria (ENP), Hondutel, Empresa Nacional de Energía Eléctrica (ENEE), y
el Instituto de Desarrollo Comunitario de Agua y Saneamiento (IDECOAS).</br>

3 ¿Cómo puedo buscar un proyecto?</br>
Para buscar información sobre el proyecto que deseas solo debes colocar alguna palabra clave
relacionada a la ubicación del proyecto, por ejemplo, nombre de la ciudad o municipio. Si
tienes el nombre del proyecto también puedes colocarlo.
Para más información sobre el uso del sistema puedes descargar el Manual de Usuario
ciudadano aquí.</br>

4. ¿Por qué no encuentro el proyecto que busco?</br>
Antes de buscar un proyecto es importante que trates de investigar cual es la entidad está a
cargo del proyecto, tomando en cuenta que no todas las entidades a cargo de ejecutar
proyectos de infraestructura pública se encuentran divulgando en este portal. Por ejemplo, los
proyectos ejecutados bajo modalidad de Alianza Público-Privada se encuentran en el portal de
transparencia de APP, al cual puedes acceder haciendo clic aquí.
Si el proyecto que buscas pertenece a alguna de las entidades obligadas a divulgar información
aquí pero aun así no encuentras la información, es probable que esta aun no haya sido
publicada. Para verificar si es este el caso puedes enviar una pregunta en la viñeta de
“contáctenos”.</br>

5. ¿Qué tipo de información puedo encontrar en SISOCS?</br>
En el Sistema de Información y Seguimiento de Obras y Contratos de Supervisión (SISOCS)
podrás encontrar toda la información de cada una de las etapas en la que se encuentran los
proyectos de infraestructura ejecutados por las entidades antes mencionadas. Esta
información corresponde a 66 puntos de datos del Estándar de la Iniciativa de Transparencia
en Infraestructura CoST Honduras (link a website de costhonduras.hn)
Algunos de estos datos son: nombre de las empresas participantes en la licitación, y nombre
de la empresa adjudicada, el contrato de construcción y supervisión, el costo inicial y final del
proyecto, razones de modificaciones de los contratos, entre otros.</br>

¿Tienes alguna otra pregunta? Envíala a nuestro formulario de contacto.</br>
